# Data Type: Type Of Data
# String[str]: Text: Address, Name, City, Country, etc
# int: Number: Age, Id: 5
# float: Decimal Number: Salary 5.1, 5.0
# bool: True or False
# list: Multiple Data: [1,2,3,4,5]

name = "Bimal Sharma" # String
age = 21
address = "Kathmandu, Nepal"
is_married = False
salary = 45000.23

print(f"The type of name is {type(name)}")
print(f"The type of age is {type(age)}")
print(f"The type of address is {type(address)}")
print(f"The type of ismarried is {type(is_married)}")
print(f"The type of salary is {type(salary)}")

